export const trendItems = itemSeedGroups.flatMap((group) =>
  group.seeds.map(([id, name, value, displayValue]) => {
    const image = createTrendItemImage({
      id,
      name,
      category: group.category,
    });

    return {
      id,
      category: group.category,
      name,
      value,
      unit: group.unit,
      displayValue,
      imageUrl: image.src,
      imageAlt: image.alt,
      sourceName: group.sourceName,
      sourceUrl: group.sourceUrl,
      updatedAt,
      ...(group.note ? { note: group.note } : {}),
    };
  }),
) satisfies TrendItem[];

export function getItemsByCategory(category: TrendCategory) {
  return trendItems.filter((item) => item.category === category);
}

export function getRandomCategoryItems(randomValue = Math.random()) {
  const categories = Array.from(
    new Set(trendItems.map((item) => item.category)),
  ) as TrendCategory[];
  const index = Math.floor(randomValue * categories.length) % categories.length;
  return getItemsByCategory(categories[index]);
}
