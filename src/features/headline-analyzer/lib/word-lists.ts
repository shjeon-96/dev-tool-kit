/**
 * Headline type detection patterns
 */
export const HEADLINE_PATTERNS = {
  "how-to": /^how\s+to\b/i,
  listicle:
    /^\d+\s+\w+|top\s+\d+|best\s+\d+|\d+\s+ways|\d+\s+tips|\d+\s+reasons/i,
  question:
    /\?$|^(what|why|how|when|where|who|which|is|are|do|does|can|should|would|will)\b/i,
  command:
    /^(get|start|stop|learn|discover|try|find|create|build|make|use|check|read|watch|download)\b/i,
  news: /^(breaking|just\s+in|update|announced|reveals?|launches?|introduces?)\b/i,
  comparison:
    /\bvs\.?\b|\bversus\b|\bcompared?\b|\bbetter\s+than\b|\bor\b.*\?$/i,
  guide:
    /\b(guide|tutorial|walkthrough|step-by-step|complete|ultimate|beginner'?s?)\b/i,
  review:
    /\b(review|rating|pros?\s+and\s+cons?|tested|hands-on|first\s+look)\b/i,
};
